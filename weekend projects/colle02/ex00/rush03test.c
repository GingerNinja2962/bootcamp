int		rush03check(char *s1, int x, int y)
{
	char	*ss[4];
	char	f[4];
	char	m[4];
	char	l[4];

	f[0] = 'A';
	m[0] = 'B';
	l[0] = 'A';
	f[1] = 'B';
	m[1] = ' ';
	l[1] = 'B';
	f[2] = 'C';
	m[2] = 'B';
	l[2] = 'C';
	ss[0] = f;
	ss[1] = m;
	ss[2] = l;
	if (rushtest(ss, x, s1, y) == 1)
		return (1);
	return (0);
}
